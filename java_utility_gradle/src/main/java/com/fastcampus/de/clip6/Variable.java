package com.fastcampus.de.clip6;

public class Variable {
    public static void main(String[] args) {

        // 변수와 상수 선언하는 방법
        int number = 1;
        System.out.println(number);

        String str = "hello world";
        System.out.println(str);

        str = "good by world";
        System.out.println(str);

        // camel case 이해하기
        int myVariable = 2;
        int myNumberVariable = 3;
    }
}
